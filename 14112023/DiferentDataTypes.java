public class DiferentDataTypes {
    public static void main(String[] args) {
        int myNum = 5;               // declara um número inteiro
        float myFloatNum = 5.99f;    // declara um número com ponto flutuante
        char myLetter = 'D';         // declara uma variavel do tipo char, armazena um caractere
        boolean myBool = true;       // declara uma variavel do tipo booleano 
        String myText = "Hello";     // declara uma variavel do tipo string para amarzenar um texto    
        System.out.println(myNum);    // dessa linha em diante será apenas os prints na tela das variaveis criadas
        System.out.println(myFloatNum);
        System.out.println(myLetter);
        System.out.println(myBool);
        System.out.println(myText);
    }
}
